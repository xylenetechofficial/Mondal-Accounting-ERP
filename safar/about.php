                        Mr Kumar realised that this is going to be a panicky situation. As they didn't have any sparer tyres to change. His parents were slightly restless due to the heat and tiredness. Mr Kumar was running from pillar to post in search of a mechanic. He frantically tried various car service numbers available on the net, but all in vain. Unfortunately, he had to take lift from a passer-by to the nearest petrol pump. Eventually, he got both tyres fixed at a puncture centre. And reached the site of his stranded car along with a motor mechanic, who charged exorbitant price for this off hours service. But as they say, all is well that ends well. Finally, they started driving back at 12:30 am at night.”
