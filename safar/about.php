                        " It was a hot summer late afternoon. Mr Kumar was travelling to Delhi via the Faridabad highway with his parents. As his driver was on leave, he had to drive his Swift Desire all by himself. Despite the scorching heat he was enjoying his pleasant drive on the countryside.
